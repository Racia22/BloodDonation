    private boolean isFormValid() {
    return !nametxt.getText().trim().isEmpty()
        && mailtxt.getText().trim().matches("^[\\w-.]+@([\\w-]+\\.)+[\\w-]{2,4}$")
        && String.valueOf(pwdtxt.getPassword()).trim().length() >= 6;
    }
    
    private void registerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerBtnActionPerformed
         // TODO add your handling code here:
         nametxtActionPerformed(evt);
         mailtxtActionPerformed(evt);
         pwdtxtActionPerformed(evt);
         
         
         if (isFormValid()) {
        User user = new User(
            0, // ID will be auto-generated
            // CHANGE: Fixed constructor argument order to match User(int id, String email, String username, String password).
            // PURPOSE: Prevent swapped username/email values being saved to DB and breaking login/tests.
            mailtxt.getText().trim(),
            nametxt.getText().trim(),
            String.valueOf(pwdtxt.getPassword()).trim()
        );
         
         UserDao userdao = new UserDao();
         
         
         if (userdao.addUser(user)) {
            JOptionPane.showMessageDialog(this, 
                "Registration successful!\nWelcome " + user.getUsername(),
                "Success", JOptionPane.INFORMATION_MESSAGE);
            new Dashboard().setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this,
                "Registration failed. Please try again.",
                "Error", JOptionPane.ERROR_MESSAGE);
        }
      }
    }//GEN-LAST:event_registerBtnActionPerformed

    
