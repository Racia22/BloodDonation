    private void pwdtxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pwdtxtActionPerformed
        // TODO add your handling code here:
        String password = String.valueOf(pwdtxt.getPassword()).trim();
    
    if (password.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Password cannot be empty", 
            "Validation Error", JOptionPane.ERROR_MESSAGE);
        pwdtxt.setBorder(BorderFactory.createCompoundBorder(
            BorderFactory.createEmptyBorder(8, 0, 8, 0),
            BorderFactory.createMatteBorder(0, 0, 2, 0, Color.RED)));
    }   
    else if (password.length() < 6) {
        JOptionPane.showMessageDialog(this, "Password must be at least be 6 characters",
            "Validation Error", JOptionPane.ERROR_MESSAGE);
        pwdtxt.setBorder(BorderFactory.createCompoundBorder(
            BorderFactory.createEmptyBorder(8, 0, 8, 0),
            BorderFactory.createMatteBorder(0, 0, 2, 0, Color.RED)));
    }
    else {
        pwdtxt.setBorder(BorderFactory.createCompoundBorder(
            BorderFactory.createEmptyBorder(8, 0, 8, 0),
            BorderFactory.createMatteBorder(0, 0, 2, 0, new Color(0, 102, 204))));
    }
    }//GEN-LAST:event_pwdtxtActionPerformed

    private void showError(String message, JTextField field) {
    JOptionPane.showMessageDialog(this, message, 
        "Validation Error", JOptionPane.ERROR_MESSAGE);
    field.setBorder(BorderFactory.createCompoundBorder(
        BorderFactory.createEmptyBorder(8, 0, 8, 0),
        BorderFactory.createMatteBorder(0, 0, 2, 0, Color.RED)));
    }
    
    
